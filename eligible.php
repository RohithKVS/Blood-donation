<h3>Donors Guidelines<br><br>
Eligibilities<br>
</h3>
<p>
Do donate blood only, if you have satisfied all of the following conditions.
<br>
<ul style="list-style-image: url('do.jpg');">
<li style="padding-bottom:10px;">You must be healthy and fit without suffering from any disease.</li>
<li style="padding-bottom:10px;">You must be 18-60 years age.</li>
<li style="padding-bottom:10px;">You must have a minimum weight of 50Kg.</li>
<li style="padding-bottom:10px;">Your Hemoglobin level must be 12.5% minimum.</li>
<li style="padding-bottom:10px;">Your pulse rate should be between 50 to 100.</li>
<li style="padding-bottom:10px;">Your BP should be normal. It should not exceed Diastolic 100mm Hg and Systolic 180 mm Hg.</li>
</ul>
</p>
<h3>Non-Eligibilities<br></h3>
<p>
Do not donate blood, if you have any one of the following conditions.
<br>
<ul style="list-style-image: url('dont.jpg');">
<li style="padding-bottom:10px;">If you have suffered with cold/fever in the past 1 week.</li>
<li style="padding-bottom:10px;">Cardiac problems, hypertension, history of cancer.</li>
<li style="padding-bottom:10px;">If you have undergone major surgery in the last 6 months.</li>
<li style="padding-bottom:10px;">If you consumed alcohol within the last 24 hours.</li>
<li style="padding-bottom:10px;">If you are HIV+.</li>
</ul>
</p>
